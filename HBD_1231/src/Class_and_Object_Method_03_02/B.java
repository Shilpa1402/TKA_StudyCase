package Class_and_Object_Method_03_02;

public class B {
	double calAreaTriangle(double b, double h) {
		double area  = (b*h)*1/2;
		return area;
	}
	double cube( double side) {
		double areac = side * side*side;
		return areac;

	}

	double areofSpare(double a){
		double spare = (4*3.14*a*a*a)/3;
		return spare;


	}

}
